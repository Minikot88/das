function role(key, label, accepts, multiple = false) {
  return {
    key,
    label,
    accepts,
    multiple,
  };
}

function createTemplate({
  id,
  family,
  variant,
  type,
  name,
  description,
  requiredRoles,
  roles,
  defaultMapping,
  defaultSettings,
}) {
  return {
    id,
    engine: "chartjs",
    family,
    variant,
    type,
    name,
    description,
    requiredRoles,
    roles,
    defaultMapping,
    defaultSettings,
  };
}

