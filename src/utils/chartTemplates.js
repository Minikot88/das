export function getChartJsTemplateById(id) {
  return chartJsTemplates.find((template) => template.id === id) ?? chartJsTemplates[0];
}

export function getChartJsTemplatesByFamily(family) {
  return chartJsTemplates.filter((template) => template.family === family);
}

export function getAllChartJsFamilies() {
  return Array.from(new Set(chartJsTemplates.map((template) => template.family)));
}

export function getChartTypes() {
  return chartJsTemplates.map(({ id, family, variant, type, name, description }) => ({
    id,
    family,
    variant,
    type,
    name,
    description,
  }));
}

